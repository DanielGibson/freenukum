void fn_borders_blit_tile(
    SDL_Surface * screen,
    SDL_Surface * tile,
    int x,
    int y,
    Uint8 pixelsize)
{
  SDL_Rect dstrect;
  dstrect.x = FN_PART_WIDTH/2 * pixelsize * x;
  dstrect.y = FN_PART_HEIGHT/2 * pixelsize * y;
  SDL_BlitSurface(
      tile,
      NULL,
      screen,
      &dstrect);
}

